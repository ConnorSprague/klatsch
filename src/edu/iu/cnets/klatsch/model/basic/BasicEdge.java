package edu.iu.cnets.klatsch.model.basic;

import edu.iu.cnets.klatsch.model.Edge;
import edu.iu.cnets.klatsch.model.Node;


/**
 * This is a simple subclass of {@link Edge} for feed types that don't need any advanced features.
 */
public class BasicEdge extends Edge
{
	public BasicEdge(Node src, Node dst, int startTime, int endTime, double weight, double trust)
	{
		super(src, dst, startTime, endTime, weight, trust);
	}
	
	
	public BasicEdge(Node src, Node dst, double weight, double trust)
	{
		super(src, dst, weight, trust);
	}
	
	
	public BasicEdge(Node src, Node dst)
	{
		super(src, dst);
	}
	
	
	public BasicEdge(Edge e1, Edge e2)
	{
		super(e1, e2);
	}
}
